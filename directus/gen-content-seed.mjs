// Generates directus/seed-content.sql: the 8 UI languages and the 3 legal pages
// (English bodies read from directus/legal/*.en.md). Run this BEFORE seed-i18n.sql.
//
//   node directus/gen-content-seed.mjs > directus/seed-content.sql
//
// Other languages for the legal pages are added later in the Directus
// Translations interface; the client renders the requested language, falling
// back to English, then to its bundled copy.

import { readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const q = (s) => `'${String(s).replace(/'/g, "''")}'`;

const VERSION = '2026-06-10';
const LANGUAGES = [
  ['it', 'Italiano'],
  ['en', 'English'],
  ['es', 'Español'],
  ['fr', 'Français'],
  ['de', 'Deutsch'],
  ['pt', 'Português'],
  ['ja', '日本語'],
  ['zh', '中文'],
];
const PAGES = [
  ['terms', 'Terms of Service', 'terms.en.md'],
  ['privacy', 'Privacy Policy', 'privacy.en.md'],
  ['acceptable-use', 'Acceptable Use Policy', 'acceptable-use.en.md'],
];

const out = [];
out.push('-- AUTO-GENERATED by directus/gen-content-seed.mjs — do not edit by hand.');
out.push('-- Seeds the UI languages and the legal pages (English). Run FIRST.');
out.push('BEGIN;');

out.push('');
out.push('-- Languages (Directus translations collection).');
LANGUAGES.forEach(([code, name], i) => {
  out.push(
    `INSERT INTO languages (code, name, sort) VALUES (${q(code)}, ${q(name)}, ${i}) ` +
      `ON CONFLICT (code) DO UPDATE SET name = EXCLUDED.name, sort = EXCLUDED.sort;`,
  );
});

out.push('');
out.push('-- Legal pages + English body (markdown).');
for (const [slug, title, file] of PAGES) {
  const body = readFileSync(join(here, 'legal', file), 'utf8').trimEnd();
  out.push(
    `INSERT INTO legal_pages (slug, version) VALUES (${q(slug)}, ${q(VERSION)}) ` +
      `ON CONFLICT (slug) DO UPDATE SET version = EXCLUDED.version;`,
  );
  out.push(
    `INSERT INTO legal_translations (page_id, language, title, body)\n` +
      `  SELECT id, 'en', ${q(title)}, ${q(body)} FROM legal_pages WHERE slug = ${q(slug)}\n` +
      `  ON CONFLICT (page_id, language) DO UPDATE SET title = EXCLUDED.title, body = EXCLUDED.body;`,
  );
}

out.push('COMMIT;');
process.stdout.write(out.join('\n') + '\n');
