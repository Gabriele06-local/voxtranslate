// Generates SQL to seed `i18n_strings` + `i18n_translations` from the client's
// bundled I18N map, so the Directus Translations UI starts fully populated with
// every UI string in every shipped language. The client treats DB rows as
// overrides over its bundled defaults, so re-seeding is always safe.
//
//   node directus/gen-i18n-seed.mjs > directus/seed-i18n.sql
//
// (Node 23.6+/24 strips the TypeScript types in i18n.ts on import. i18n.ts reads
// navigator.language at load; Node's global `navigator` exists with language
// undefined, so detectLang() just falls back to 'en' — no stub needed.)

const { I18N, SUPPORTED } = await import('../client/src/scripts/i18n.ts');

const q = (s) => `'${String(s).replace(/'/g, "''")}'`;

// Union of every key across languages, in English order first.
const keys = [];
const seen = new Set();
for (const lang of [...SUPPORTED]) {
  for (const k of Object.keys(I18N[lang] || {})) {
    if (!seen.has(k)) {
      seen.add(k);
      keys.push(k);
    }
  }
}

const out = [];
out.push('-- AUTO-GENERATED by directus/gen-i18n-seed.mjs — do not edit by hand.');
out.push('-- Seeds every bundled UI string into the managed i18n tables.');
out.push('-- Run AFTER seed-content.sql (which creates the languages rows).');
out.push('BEGIN;');

for (const k of keys) {
  out.push(`INSERT INTO i18n_strings (key) VALUES (${q(k)}) ON CONFLICT (key) DO NOTHING;`);
}
out.push('');

for (const lang of [...SUPPORTED]) {
  const dict = I18N[lang] || {};
  for (const k of keys) {
    const v = dict[k];
    if (v == null) continue;
    out.push(
      `INSERT INTO i18n_translations (string_id, language, value)\n` +
        `  SELECT id, ${q(lang)}, ${q(v)} FROM i18n_strings WHERE key = ${q(k)}\n` +
        `  ON CONFLICT (string_id, language) DO NOTHING;`,
    );
  }
}

out.push('COMMIT;');
process.stdout.write(out.join('\n') + '\n');
